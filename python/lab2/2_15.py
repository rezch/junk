# Вставить список В между k-м и (k + 1)-м элементами списка А (k задано).

def f(a: list, b: list, k: int) -> list:
    return a[:k + 1] + b + a[k + 1:]

a = [1, 2, 3, 4, 5, 6]
b = [-4, -3, -2, -1]
k = 2
print(f(a, b, k))

